    ['name' => 'Geleneksel Tarım', 'color' => '#228be6', 'description' => 'Geleneksel Türk tarımı'],
];

foreach ($productionTypes as $pt) {
    $existing = $db->fetch("SELECT id FROM production_types WHERE company_id = ? AND name = ?", [$companyId, $pt['name']]);
    if (!$existing) {
        $db->insert('production_types', [
            'id' => $db->generateUuid(),
            'company_id' => $companyId,
            'name' => $pt['name'],
            'slug' => strtolower(str_replace(['ı', 'ö', 'ü', 'ş', 'ğ', 'ç', ' '], ['i', 'o', 'u', 's', 'g', 'c', '-'], $pt['name'])),
