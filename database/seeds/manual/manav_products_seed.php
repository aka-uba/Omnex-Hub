$db = Database::getInstance();

// Önce mevcut firma ID'sini al
$company = $db->fetch("SELECT id FROM companies LIMIT 1");
if (!$company) {
    die("Firma bulunamadı. Önce bir firma oluşturun.\n");
}
$companyId = $company['id'];

$isCli = php_sapi_name() === 'cli';
if ($isCli) {
    echo "Firma ID: $companyId\n";
    echo "Manav ürünleri ekleniyor...\n\n";
