            'sort_order' => 0,
            'status' => 'active',
            'created_at' => date('Y-m-d H:i:s'),
            'updated_at' => date('Y-m-d H:i:s')
        ]);
        if ($isCli) echo "Üretim tipi eklendi: {$pt['name']}\n";
    }
}

